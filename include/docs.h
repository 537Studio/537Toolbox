//圆角矩形 

void roundrect(//无填充版 
	int left,
	int top,
	int right,
	int bottom,
	int ellipsewidth,
	int ellipseheight
);

void fillroundrect(//填充版 
	int left,
	int top,
	int right,
	int bottom,
	int ellipsewidth,
	int ellipseheight
);
/*
left
圆角矩形左部 x 坐标。

top
圆角矩形顶部 y 坐标。

right
圆角矩形右部 x 坐标。

bottom
圆角矩形底部 y 坐标。

ellipsewidth
构成圆角矩形的圆角的椭圆的宽度。

ellipseheight
构成圆角矩形的圆角的椭圆的高度。
*/
